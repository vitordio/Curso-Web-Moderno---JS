// Procedural - procedimento baseado na função. Função que manipula dados.
processamento(valor1, valor2, valor3)

// OO - mudou o foco. Você tem dados e dentro desses dados vc tem funções.
// OO funciona como essa capsula que agrupa comportamentos e atributos.
objeto = {
    valor1, valor2, valor3,
    processamento() {
        // ...
    }
}

objeto.processamento(); // Foco passou a ser o objeto

// Principios importantes de OO
// 1. Abstração - Pegar o objeto do mundo real e abstrair apenas informações necessárias para o sistema.
// 2. Encapsulamento - detalhes da implementação escondidos e mostrar apenas para quem precisa usar.
// 3. Herança (prototype)
// 4. Polimorfismo - multiplas formas
